package com.ao.recyclerview_snapping.fork;

public class Contact_fork {
	String nameOne;
	String nameTow;

	public Contact_fork(String nameOne, String nameTow) {
		this.nameOne = nameOne;
		this.nameTow = nameTow;
	}

	public String getNameOne() {
		return nameOne;
	}

	public void setNameOne(String nameOne) {
		this.nameOne = nameOne;
	}

	public String getNameTow() {
		return nameTow;
	}

	public void setNameTow(String nameTow) {
		this.nameTow = nameTow;
	}
}
